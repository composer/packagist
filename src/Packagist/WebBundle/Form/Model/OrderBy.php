namespace Packagist\WebBundle\Form\Model;

/**
 * @author Benjamin Michalski <benjamin.michalski@gmail.com>
 */
class OrderBy
{
    /**
     * @var string
     */
    protected $sort;

    /**
     * @var string
     */
    protected $order;

    public function setSort($sort)
    {
        $this->sort = $sort;
    }

    public function getSort()
    {
        return $this->sort;
    }

    public function setOrder($order)
    {
        $this->order = $order;
    }

    public function getOrder()
    {
        return $this->order;
    }
}